package com.androidnerds.tictactoe.game.model;

/**
 * Enum representing the state of the cell.
 * This class holds three states
 * FREE - cell is free and available.
 * CROSS - cell is occupied by Player 1.
 * CIRCLE - cell is occupied by Player 2.
 */
public enum CellState {
    FREE,
    CROSS,
    CIRCLE
}
